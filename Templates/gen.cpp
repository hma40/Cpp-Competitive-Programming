int best_b(vt<int> a) {
    int n = a.size();
    vector<int> best_b;
    // mask from 0 to (1<<(n-1)) - 1 encodes cuts after positions 0..n-2
    for (int mask = 0; mask < (1 << (n - 1)); mask++) {
        vector<int> b;
        int cur_min = a[0];
        // scan positions 0..n-2, decide if we cut after i
        for (int i = 0; i < n - 1; i++) {
            cur_min = min(cur_min, a[i]);
            if (mask & (1 << i)) {
                // cut here
                b.push_back(cur_min);
                cur_min = a[i + 1];
            }
        }
        // last segment ends at n-1
        cur_min = min(cur_min, a[n - 1]);
        b.push_back(cur_min);

        if (mask == 0 || b > best_b) {
            best_b = move(b);
        }
    }
    // cout << a << best_b << endl;
    return best_b.size();
}
