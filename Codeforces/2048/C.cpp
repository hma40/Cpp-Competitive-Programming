string xr(int l, int r) {
    // cout << l << " " << r << endl;
    string orig = s;
    l=n-l-1;
    r=n-r-1;
    swap(l,r);
    for(int i = l; i <= r; i++) {
        bool o = orig[i-l]=='1';
        bool nr = orig[i]=='1';
        // cout << i << " " << o << " " << nr << endl;
        if(o==1&&nr==0) {
            orig[i-l]='1';
        } else if(o==0&&nr==1) {
            orig[i-l]='1';
        } else {
            orig[i-l]='0';
        }
    }
    reverse(begin(orig),end(orig));
    // cout << orig << endl;
    return orig;
}
