struct DSU {
    int n;
    vt<int> par;
    vt<int> sz;
    DSU(int nn): n(nn) {
        par.resize(nn, -1);
        sz.resize(nn, 1);
    }
    int get(int x) {
        if(par[x]==-1) return x;
        return par[x]=get(par[x]);
    }  
    bool unite(int u, int v) {
        u=get(u);
        v=get(v);
        if(u==v) return false;
        if(sz[u]>sz[v]) swap(u,v);
        sz[v]+=sz[u];
        par[u]=v;
        return true;
    }
};
