int lesser(vt<int> &v, int x) {
    auto it = std::lower_bound(v.begin(), v.end(), x);
    // cout << v << x << endl;
    // If the iterator points to the beginning, no such element exists
    if (it == v.begin()) {
        return -1;
    }

    // Otherwise, return the index of the largest element less than x
    // cout << "RETURNING " << std::distance(v.begin(), it) - 1 << endl;
    return v[std::distance(v.begin(), it) - 1];
}
